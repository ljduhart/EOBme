def merge_rules(*rule_dicts: dict[str, set[str]]) -> dict[str, set[str]]:
    merged: dict[str, set[str]] = defaultdict(set)
    for rules in rule_dicts:
        for column1, column2_codes in rules.items():
            merged[column1].update(column2_codes)
    return {key: set(values) for key, values in merged.items()}


