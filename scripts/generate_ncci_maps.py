def format_set(values: set[str]) -> str:
    return ", ".join(f'"{code}"' for code in sorted(values))


def format_map(rules: dict[str, set[str]], indent: str = "        ") -> str:
    lines = []
    for column1 in sorted(rules.keys()):
        lines.append(f'{indent}"{column1}" to setOf({format_set(rules[column1])}),')
    return "\n".join(lines)


def write_kotlin_object(filename: str, object_name: str, rules: dict[str, set[str]], description: str) -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    path = OUTPUT_DIR / filename
    pair_lines: list[str] = []
    for column1 in sorted(rules.keys()):
        for column2 in sorted(rules[column1]):
            pair_lines.append(f"{column1}|{column2}")
    chunks = [pair_lines[i:i + 500] for i in range(0, len(pair_lines), 500)]
    chunk_defs = []
    chunk_names = []
    for index, chunk in enumerate(chunks):
        chunk_name = f"pairLinesChunk{index}"
        chunk_names.append(chunk_name)
        lines = ",\n            ".join(f'"{line}"' for line in chunk)
        chunk_defs.append(f"    private val {chunk_name} = listOf(\n            {lines}\n    )")
    chunk_block = "\n\n".join(chunk_defs)
    chunk_refs = " + ".join(chunk_names)
    content = f"""package {PACKAGE}

/**
 * {description}
 * Column-one CPT codes map to bundled column-two codes per CMS NCCI PTP edits.
 */
object {object_name} {{
{chunk_block}

    private val pairLines: List<String> = {chunk_refs}

    val rules: Map<String, Set<String>> by lazy {{
        NcciBundlingSupport.rulesFromLines(pairLines)
    }}
}}
"""
    path.write_text(content, encoding="utf-8")


def write_master_map(category_maps: list[tuple[str, str]]) -> None:
    merge_lines = []
    for object_name, _ in category_maps:
        merge_lines.append(f"            {object_name}.rules,")
    merge_body = "\n".join(merge_lines)
    content = f"""package {PACKAGE}

/**
 * Master aggregation of local CMS NCCI PTP bundling rules.
 */
object NcciBundlingMap {{
    val ncciBundlingRules: Map<String, Set<String>> by lazy {{
        NcciBundlingSupport.mergeBundlingRules(
{merge_body}
        )
    }}

    fun bundledCodesFor(cptCode: String): Set<String>? {{
        val normalized = cptCode.trim().uppercase()
        if (normalized.isBlank()) return null
        return ncciBundlingRules[normalized]
    }}

    val totalPairCount: Int
        get() = ncciBundlingRules.values.sumOf {{ it.size }}
}}
"""
    (OUTPUT_DIR / "NcciBundlingMap.kt").write_text(content, encoding="utf-8")


