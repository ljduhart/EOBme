    categories = [
        ("NcciEmBundlingMap.kt", "NcciEmBundlingMap", build_em_rules(), "E&M and office visit NCCI bundling rules."),
        ("NcciLabBundlingMap.kt", "NcciLabBundlingMap", build_lab_rules(), "Laboratory and pathology NCCI bundling rules."),
        ("NcciSurgeryBundlingMap.kt", "NcciSurgeryBundlingMap", build_surgery_rules(), "Surgery and orthopedics NCCI bundling rules."),
        ("NcciRadiologyBundlingMap.kt", "NcciRadiologyBundlingMap", build_radiology_rules(), "Radiology NCCI bundling rules."),
        ("NcciSpecialtyBundlingMap.kt", "NcciSpecialtyBundlingMap", build_specialty_rules(), "Integumentary, cardiovascular, GI, and PT NCCI bundling rules."),
    ]
    category_maps: list[tuple[str, str]] = []
    for filename, object_name, rules, description in categories:
        write_kotlin_object(filename, object_name, cap_rules(rules), description)
        category_maps.append((object_name, filename))
        print(f"{object_name}: {pair_count(cap_rules(rules))} pairs across {len(rules)} column-one codes")
    write_master_map(category_maps)
    merged = merge_rules(*(cap_rules(rules) for _, _, rules, _ in categories))
    total = pair_count(merged)
    print(f"Merged total: {total} pairs across {len(merged)} column-one codes")
    if total < 2500:
        raise SystemExit(f"Expected at least 2500 pairs, generated {total}")


